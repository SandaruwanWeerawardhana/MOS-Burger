// add cart
let CartArray = [];

function addCart(index) {
  const selectItem = items[index];
  const existingItem = CartArray.find(item => item.Code === selectItem.itemCode);

  if (existingItem) {
    existingItem.qty += 1; 
  } else {
    CartArray.push({
      Code: selectItem.itemCode,
      Name: selectItem.itemName,
      Price: selectItem.price,
      img: selectItem.img, 
      qty: 1,
    });
  }
  console.log(CartArray);
  
  displayCart();
}

// Display Cart
function displayCart() {
  const AddCart = document.getElementById("cartbox");
  let TempAddCart = ``;

  CartArray.forEach((element, index) => {
    TempAddCart += `
      <tr>
        <td>${element.Code}</td>
        <td>${element.Name}</td>
        <td>Rs.${element.Price}</td>
        <td><img src="${element.img}" alt="${element.Name}" class="item-image" style="width: 50px; height: auto;"></td>
        <td>${element.qty}</td>
        <td>
          <button class="btn btn-sm btn-outline-danger" onclick="removeCartItem(${index})">üóëÔ∏è</button>
        </td>
      </tr>
    `;
  });

  AddCart.innerHTML = TempAddCart; 
}

// Remove  Cart
function removeCartItem(index) {
  CartArray.splice(index, 1); 
  displayCart(); 
}

